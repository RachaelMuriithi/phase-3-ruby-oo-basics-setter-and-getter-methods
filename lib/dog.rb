class Dog
    # setter for the name of the dog
    def name=(name)
      @name = name
    end
  
    # setter for the breed  of the dog
    def breed=(breed)
      @breed = breed
    end
  
    # getter for the name of the dog
    def name
      @name
    end
  
    # getter for the name of the dog
    def breed
      @breed
    end
  end